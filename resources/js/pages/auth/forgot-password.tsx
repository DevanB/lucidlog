// Components

import { Form, Head } from '@inertiajs/react'
import { LoaderCircle } from 'lucide-react'
import InputError from '@/components/input-error'
import TextLink from '@/components/text-link'
import { Button } from '@/components/ui/button'
import { Input } from '@/components/ui/input'
import { Label } from '@/components/ui/label'
import AuthLayout from '@/layouts/auth-layout'
import { login } from '@/routes'
import { email } from '@/routes/password'

export default function ForgotPassword({ status }: { status?: string }) {
  return (
    <AuthLayout description="Enter your email to receive a password reset link" title="Forgot password">
      <Head title="Forgot password" />

      {status && <div className="mb-4 text-center font-medium text-green-600 text-sm">{status}</div>}

      <div className="space-y-6">
        <Form {...email.form()}>
          {({ processing, errors }) => (
            <>
              <div className="grid gap-2">
                <Label htmlFor="email">Email address</Label>
                <Input
                  autoComplete="off"
                  autoFocus
                  id="email"
                  name="email"
                  placeholder="email@example.com"
                  type="email"
                />

                <InputError message={errors.email} />
              </div>

              <div className="my-6 flex items-center justify-start">
                <Button className="w-full" data-test="email-password-reset-link-button" disabled={processing}>
                  {processing && <LoaderCircle className="h-4 w-4 animate-spin" />}
                  Email password reset link
                </Button>
              </div>
            </>
          )}
        </Form>

        <div className="space-x-1 text-center text-muted-foreground text-sm">
          <span>Or, return to</span>
          <TextLink href={login()}>log in</TextLink>
        </div>
      </div>
    </AuthLayout>
  )
}
